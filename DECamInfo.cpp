map<string,Device>
decam::decamInfo() {
  map<string,Device> info;
  {
    istringstream iss(ccdData);
    string buffer;
    DECamMap map;
    while (getlineNoComment(iss, buffer)) {
      istringstream iss(buffer);
      string name;
      Device d;
      if (!(iss >> name >> d.ccdnum >> d.detsecX >> d.detsecY)) {
	cerr << "Bad Device line: " << buffer << endl;
	exit(1);
      }
      map.setDevice(name);
      Bounds<double> b;
      double x,y;
      map.toField(1.,1.,x,y);
      b += Position<double>(x,y);
      map.toField(1.,2048.,x,y);
      b += Position<double>(x,y);
      map.toField(1.,4096.,x,y);
      b += Position<double>(x,y);
      map.toField(1024.,4096.,x,y);
      b += Position<double>(x,y);
      map.toField(2048.,4096.,x,y);
      b += Position<double>(x,y);
      map.toField(2048.,2048.,x,y);
      b += Position<double>(x,y);
      map.toField(2048.,1.,x,y);
      b += Position<double>(x,y);
      map.toField(1024.,1.,x,y);
      b += Position<double>(x,y);
      d.b = b;
      d.norm = 1.;
      info[name] = d;
    }
  }
  return info;
}

void
decam::getDeviceNorms(string filename, map<string,Device>& devices) {
  ifstream ifs(filename.c_str());
  if (!ifs) {
    cerr << "could not open device norms file " << filename << endl;
    exit(1);
  }
  string buffer;
  while (getlineNoComment(ifs, buffer)) {
    istringstream iss(buffer);
    string detpos;
    double norm;
    if (!(iss >> detpos >> norm)) {
      cerr << "Bad input line in device norm file: <" << buffer << ">" << endl;
      exit(1);
    }
    if (devices.count(detpos)==0) {
      cerr << "Unknown DETPOS in norm file: <" << detpos << ">" << endl;
      exit(1);
    }
    devices[detpos].norm = norm;
  }
}

DECamMap::DECamMap(): prefix("i/"), wcs(0) {
  istringstream iss(referenceMap);
  pmc.read(iss);

  // Shift all field coordinates so that center of N4 & S4 is origin:
  Bounds<double> b;
  double x,y;
  pmc.issueWcs(prefix + "N4")->toWorld(1024.,2048.,x,y);
  b += Position<double>(x,y);
  pmc.issueWcs(prefix + "S4")->toWorld(1024.,2048.,x,y);
  b += Position<double>(x,y);
  centerX = b.center().x;
  centerY = b.center().y;
}

void
DECamMap::setDevice(string detpos) {
  wcs = pmc.issueWcs(prefix + detpos);
}

void
DECamMap::toPix(double xField, double yField, double& xPix, double& yPix) const {
  wcs->toPix( xField + centerX, yField + centerY, xPix, yPix);
  return;
}

void
DECamMap::toField(double xPix, double yPix, double& xField, double& yField) const {
  wcs->toWorld(xPix, yPix, xField, yField);
  xField -= centerX;
  yField -= centerY;
  return;
}



